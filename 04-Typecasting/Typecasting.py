# Integer to float
a = 5
b = float(a)
print("Integer to float:", b)

# Float to integer
c = 7.8
d = int(c)
print("Float to integer:", d)

# String to integer
e = "10"
f = int(e)
print("String to integer:", f)

# Integer to string
g = 15
h = str(g)
print("Integer to string:", h)

# String to float
i = "12.34"
j = float(i)
print("String to float:", j)

# Float to string
k = 9.81
l = str(k)
print("Float to string:", l)

# Boolean to integer
m = True
n = int(m)
print("Boolean to integer:", n)

# Integer to boolean
o = 0
p = bool(o)
print("Integer to boolean:", p)